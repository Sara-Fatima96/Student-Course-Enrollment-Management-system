# Student Course Enrollment management system
/* Business Problem : In a training Institute the data is stored in Excel.
As the number of students increasing manually analyzing/trcaking the data is difficult. */
/*Objective :
we need a system which tracks students details,courses,enrollments,instructors which is easy to track the details. */

Create database sqlproject;
use sqlproject;
create table students(StudentID int primary key,
studentName varchar(20),
Gender varchar(10),
Age int,
City varchar(20),
Email varchar(30),
Phone varchar(20),
JoiningDate Date);  
select * from students;

Create table Instructors(InstructorID int primary key,
InstructorName varchar(20),
Department Varchar(20),
Experience int);
select * from Instructors;

create table Courses(CourseID int primary key,
CourseName varchar(20),
DurationMonth int,
Fee Decimal(10,2),
InstructorID int,
foreign key (InstructorID)
references Instructors(InstructorID));
select * from courses;

create table Enrollments(EnrollmentID int primary key,
StudentID int,
CourseID int,
EnrollmentDate Date,
CompletionStatus varchar(20),
Marks int,
foreign key (StudentID)
references Students(StudentID),
foreign key (CourseID)
references Courses(CourseID));

select * from Enrollments;

