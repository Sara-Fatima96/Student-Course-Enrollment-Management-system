# 5. Studets Enrolled in each course
select 
c.CourseName,
count(e.StudentID) as total_students
from Courses c
join Enrollments e
on c.CourseID=e.CourseID
group by c.CourseName;
# most students are enrolled in data analytics

#6. List students with there courses
SELECT s.StudentName,
       c.CourseName
FROM Students s
JOIN Enrollments e
ON s.StudentID = e.StudentID
JOIN Courses c
ON e.CourseID = c.CourseID;

# 7. Students who complete there courses
select s.studentname,
c.coursename,
e.completionstatus
from students s
join enrollments e
on s.studentid = e.studentid
join courses c
on c.courseid = e.courseid
where completionstatus="COmpleted";

# 8. students who are still studying
select s.studentname,
c.coursename,
e.completionstatus
from students s
join enrollments e
on s.studentid = e.studentid
join courses c
on c.courseid = e.courseid
where completionstatus="Ongoing";

#9.city wise student count
select city,count(*) as total_students from students group by city;
# Using SQL Query , I have analyzed that most of the students are from Hyderabad

#10. Revenue generated by each course
select c.coursename,
count(e.studentId)* c.fee as revenue
from enrollments e 
join courses c
on e.courseid = c.courseid
group by c.coursename, c.fee;
# Highest revenue is generated from data analytics
# Least revenue is generated from excel.